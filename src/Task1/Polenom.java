package Task1;

public class Polenom {

    public int kof;
    public int step;

    public Polenom(int kof, int step) {
        this.kof =kof;
        this.step = step;
    }

    public void setKof(int kof) {
        this.kof = kof;
    }

    public int getKof() {
        return kof;
    }

    public void setStep(int step) {
        this.step = step;
    }

    public int getStep() {
        return step;
    }
}
